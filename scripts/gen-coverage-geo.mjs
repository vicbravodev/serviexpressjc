// Regenerates components/coverage-geo.ts (state paths + city node positions for
// the coverage map). One-off build tool; the generated output is committed so
// runtime needs no map libraries.
//
// Usage (from a scratch dir, not the app — keeps d3 out of app deps):
//   curl -sL -o us-states.json https://cdn.jsdelivr.net/npm/us-atlas@3/states-10m.json
//   curl -sL -o mx-states.json https://raw.githubusercontent.com/angelnmara/geojson/master/mexicoHigh.json
//   npm i d3-geo@3 topojson-client@3
//   node gen-coverage-geo.mjs /abs/path/to/components/coverage-geo.ts
import { readFileSync, writeFileSync } from "node:fs"
import { geoMercator, geoPath } from "d3-geo"
import { feature } from "topojson-client"

const usTopo = JSON.parse(readFileSync("./us-states.json", "utf8"))
const mx = JSON.parse(readFileSync("./mx-states.json", "utf8"))

// US states -> GeoJSON features. Drop non-contiguous (AK, HI) and territories
// so a single Mercator fit stays tight on the North-America landmass.
const DROP = new Set(["02", "15", "60", "66", "69", "72", "78"])
const usFeat = feature(usTopo, usTopo.objects.states).features.filter(
  (f) => !DROP.has(String(f.id)),
)

const states = [
  ...usFeat.map((f) => ({ country: "us", name: f.properties.name, geom: f })),
  ...mx.features.map((f) => ({ country: "mx", name: f.properties.name, geom: f })),
]

const collection = { type: "FeatureCollection", features: states.map((s) => s.geom) }

const W = 1000
const H = 660
const PAD = 14
const projection = geoMercator().fitExtent(
  [
    [PAD, PAD],
    [W - PAD, H - PAD],
  ],
  collection,
)
const path = geoPath(projection)

const r1 = (s) => s.replace(/-?\d+\.?\d*/g, (n) => (Math.round(parseFloat(n) * 10) / 10).toString())

const STATES = states.map((s, i) => {
  const [cx, cy] = path.centroid(s.geom)
  return {
    id: `${s.country}-${i}`,
    name: s.name,
    country: s.country,
    d: r1(path(s.geom)),
    cx: Math.round(cx * 10) / 10,
    cy: Math.round(cy * 10) / 10,
  }
})

// Key network cities (lng, lat) projected into the same space.
const CITY_COORDS = {
  monterrey: { name: "Monterrey", lnglat: [-100.316, 25.686] },
  laredo: { name: "Laredo", lnglat: [-99.507, 27.53] },
  sanantonio: { name: "San Antonio", lnglat: [-98.49, 29.42] },
  houston: { name: "Houston", lnglat: [-95.37, 29.76] },
  dallas: { name: "Dallas", lnglat: [-96.8, 32.78] },
  kansascity: { name: "Kansas City", lnglat: [-94.58, 39.1] },
  chicago: { name: "Chicago", lnglat: [-87.63, 41.88] },
  atlanta: { name: "Atlanta", lnglat: [-84.39, 33.75] },
  miami: { name: "Miami", lnglat: [-80.19, 25.76] },
  losangeles: { name: "Los Angeles", lnglat: [-118.24, 34.05] },
  cdmx: { name: "CDMX", lnglat: [-99.13, 19.43] },
  guadalajara: { name: "Guadalajara", lnglat: [-103.35, 20.66] },
  chihuahua: { name: "Chihuahua", lnglat: [-106.07, 28.63] },
  merida: { name: "Mérida", lnglat: [-89.59, 20.97] },
}
const CITIES = {}
for (const [k, v] of Object.entries(CITY_COORDS)) {
  const [x, y] = projection(v.lnglat)
  CITIES[k] = { key: k, name: v.name, x: Math.round(x * 10) / 10, y: Math.round(y * 10) / 10 }
}

const header = `// AUTO-GENERATED by scripts/gen-coverage-geo (d3-geo + us-atlas + Mexico states GeoJSON).
// Contiguous-US + Mexico states projected together with a single Mercator fit.
// Do not edit by hand; regenerate to change geometry.
`
const out =
  header +
  `export const MAP_VIEWBOX = { w: ${W}, h: ${H} } as const\n\n` +
  `export type GeoState = { id: string; name: string; country: "mx" | "us"; d: string; cx: number; cy: number }\n\n` +
  `export const STATES: GeoState[] = ${JSON.stringify(STATES)}\n\n` +
  `export type GeoCity = { key: string; name: string; x: number; y: number }\n\n` +
  `export const CITIES: Record<string, GeoCity> = ${JSON.stringify(CITIES)}\n`

const target = process.argv[2]
writeFileSync(target, out)
console.log("states:", STATES.length, "-> mx:", STATES.filter((s) => s.country === "mx").length, "us:", STATES.filter((s) => s.country === "us").length)
console.log("bytes:", out.length)
console.log("cities:", Object.keys(CITIES).length)
